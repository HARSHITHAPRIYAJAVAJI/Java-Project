package javapackage;

public class TypeCasting {
//converting a variable of one type to another data type is TC
	public static void main(String[] args) {
		int x=55;
		float y=x;
		System.out.println(y);
		double a = 30.97;
		int b= (int)a;
		System.out.println(b);
		float f = 123.45f;
		long l = (long) f; 
		System.out.println(l); 
		
	

	}

}
